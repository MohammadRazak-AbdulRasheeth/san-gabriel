/**
 * Get insights by category
 * @param {string} category - Category ID (marketing-strategy, industry-trends, business-growth, or 'all')
 * @returns {Array} Filtered insights sorted by date
 */
export const getInsightsByCategory = (category) => {
  const sorted = getInsightsByDate();
  if (category === 'all') {
    return sorted;
  }
  return sorted.filter(insight => insight.category === category);
};

/**
 * Get a single insight by slug
 * @param {string} slug - Insight slug
 * @returns {Object|undefined} Insight object or undefined
 */
export const getInsightBySlug = (slug) => {
  return insights.find(insight => insight.slug === slug);
};

/**
 * Get related insights (same category, excluding current)
 * @param {string} currentSlug - Current insight slug to exclude
 * @param {number} limit - Maximum number of related insights to return
 * @returns {Array} Related insights
 */
export const getRelatedInsights = (currentSlug, limit = 3) => {
  const current = getInsightBySlug(currentSlug);
  if (!current) return [];
  
  return insights
    .filter(insight => insight.slug !== currentSlug && insight.category === current.category)
    .slice(0, limit);
};

